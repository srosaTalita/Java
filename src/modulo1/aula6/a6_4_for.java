package modulo1.aula6;

public class a6_4_for {
    public static void main(String[] args) {
        int count = 0;
        while(count < 10){
            System.out.println(count);
            count ++;
        }

        for(int count2 = 0; count2 < 10; count2 ++){
            System.out.println(count2);
        }
    }
}
